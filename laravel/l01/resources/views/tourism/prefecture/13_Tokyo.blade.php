<h2>Vibrant Diversity and Ever-Evolving Attractions</h2>
<p style="margin-bottom: 20px;">Tokyo, bustling with over 14 million residents, is among the world's most populous cities and serves as Japan's capital. Originally a small fishing village named Edo, it grew into a major urban center over centuries. In 1868, the capital was moved from Kyoto to Edo, which was then renamed Tokyo, marking its transformation into a modern metropolis. Known for its blend of tradition and innovation, Tokyo offers a rich array of arts, entertainment, and diverse neighborhoods, making it a vibrant and unforgettable destination at the heart of Japan.</p>
