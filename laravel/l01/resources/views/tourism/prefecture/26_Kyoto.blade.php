<h2>Festivals and Events</h2>
<ul class="prefectureList02">
<li>
<h3><a href="https://kyoto.travel/en/season_festivals/aoi_matsuri.html" target="_blank" rel="noopener noreferrer">Aoi Matsuri (May)</a></h3>
<p>Traditional festival held annually in Kyoto on May 15th, featuring a grand procession of people dressed in Heian-period costumes, showcasing the city's rich cultural heritage</p>
</li>
<li>
<h3><a href="https://kyoto.travel/en/season_festivals/gion_matsuri.html" target="_blank" rel="noopener noreferrer">Gion Matsuri (July)</a></h3>
<p>One of Japan's most famous festivals, held in Kyoto throughout the month of July, featuring colorful processions of floats (yamahoko) and traditional performances, celebrating the preservation of traditions and culture</p>
</li>
<li>
<h3><a href="https://kyoto.travel/en/season_festivals/gozan_okuribi.html" target="_blank" rel="noopener noreferrer">Daimonji Gozan Okuribi (August)</a></h3>
<p>Event held on August 16th as part of the Obon festival in Kyoto, where giant bonfires are lit on the mountainsides surrounding the city, forming the shape of a kanji character "大" (meaning "big") to guide the spirits of the deceased back to the spirit world</p>
</li>
<li>
<h3><a href="https://kyoto.travel/en/season_festivals/noryoyuka_kawadoko.html" target="_blank" rel="noopener noreferrer">Kawadoko - Summer Riverside Dining</a></h3>
<p>Unique dining experience in Kyoto and Kibune during the hot summer months, where restaurants set up platforms (kawadoko) along the banks of the Kamo River in Kyoto and the Kibune River in Kibune, allowing patrons to enjoy traditional Japanese cuisine in a cool and scenic outdoor setting</p>
</li>
<li>
<h3><a href="https://kyoto.travel/en/season_festivals/jidai_matsuri.html" target="_blank" rel="noopener noreferrer">Jindai Matsuri (October)</a></h3>
<p>The Jidai Matsuri Festival in Kyoto, alongside the Aoi Matsuri and Gion Matsuri, celebrates Kyoto's founding and Emperor Kanmu's capital move. Organized by Heian Jingu Shrine, it features a grand procession on October 22, displaying historical costumes. Preceding rituals include prayers, flower offerings, and spirit transfers</p>
</li>
</ul>
