</li>
<li>
<h3><a href="https://www.kyohaku.go.jp/eng/" target="_blank" rel="noopener noreferrer">Kyoto National Museum</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/kyotomuseum.jpg" alt="Kyoto National Museum">
<p>Showcases Japan's cultural heritage with rotating exhibitions of traditional arts, crafts, and historical artifacts</p>
</li>
<li>
<h3><a href="https://www.kyoto-nishiki.or.jp/en/" target="_blank" rel="noopener noreferrer">Nishiki Market</a></h3>
<p>Lively indoor market offering a wide array of fresh seafood, produce, and traditional Kyoto delicacies</p>
</li>
<li>
<h3><a href="https://kyoto-shijo.or.jp/shop_en/" target="_blank" rel="noopener noreferrer">Shijo Shopping Street</a></h3>
<p>Bustling commercial district in downtown Kyoto, lined with department stores, boutiques, and restaurants</p>
