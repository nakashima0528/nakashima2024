<h2>Historical and Cultural Landmarks</h2>
<ul class="prefectureList02">
<li>
<h3><a href="https://www.shimogamo-jinja.or.jp/" target="_blank" rel="noopener noreferrer">Shimogamo Shrine</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/shimogamojinja.jpg" alt="Shimogamo Shrine">
<p>Over 1,300 years old, Shimogamo Shrine is one of Japan's oldest Shinto shrines. It rests in Kyoto's Shimogamo district, embraced by a peaceful forest along the Kamo River</p>
</li>
<li>
<h3><a href="https://www.shokoku-ji.jp/en/kinkakuji/" target="_blank" rel="noopener noreferrer">Rokuon-ji (Kinkaku-ji, Golden Pavilion)</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/kinkakuji.jpg" alt="Rokuon-ji (Kinkaku-ji, Golden Pavilion)">
<p>Holding a captivating tale, once a retreat for the shogun Ashikaga Yoshimitsu (1358–1408), Rokuon-ji transformed into a place of Zen contemplation. Its golden exterior symbolizes purity amidst worldly temptations</p>
</li>
<li>
<h3><a href="https://www.shokoku-ji.jp/en/ginkakuji/" target="_blank" rel="noopener noreferrer">Jisho-ji (Ginkaku-ji, Silver Pavilion)</a></h3>
<p>Originally a villa for the shogun Ashikaga Yoshimasa (1436–1490), Jisho-ji later became a Zen temple. Despite lacking its planned silver adornments, its elegance and gardens offer a serene escape</p>
</li>
<li>
<h3 style="margin-bottom: 10px;"><a href="https://www.geihinkan.go.jp/en/kyoto/" target="_blank" rel="noopener noreferrer">Kyoto State Guest House</a></h3>
<div class="video-wrapper">
<iframe width="150" height="100" src="https://www.youtube.com/embed/dj--oNwhgzM?si=uLaTVZY6bGMC-Ayd" frameborder="0" allow="accelerometer; autoplay; clipboard-write; encrypted-media; gyroscope; picture-in-picture" allowfullscreen style="margin-right: 20px;"></iframe>
<p>Official accommodation for foreign dignitaries visiting Kyoto, showcasing traditional Japanese architecture and hospitality<br><span style="font-size: 70%;">Video by State Guest Houses of Japan, © State Guest Houses of Japan.</span></p>
</div>
</li>
<li>
<h3><a href="https://nijo-jocastle.city.kyoto.lg.jp/?lang=en" target="_blank" rel="noopener noreferrer">Nijo Castle</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/nijojo.jpg" alt="Nijo Castle">
<p>Served as the residence of Japan's shoguns, symbolizing their authority. In 1867, it witnessed the historic moment when the last shogun returned authority to the Emperor, marking the end of the Edo period. Renowned for its elegant architecture, serene gardens, and unique "nightingale floors"</p>
</li>
<li>
<h3><a href="https://www.yasaka-jinja.or.jp/" target="_blank" rel="noopener noreferrer">Gion Yasaka Shrine</a></h3>
<p>Nestled in Kyoto's Gion district, dating back over a millennium, Gion Yasaka Shrine was originally established in the 7th century to protect the city from epidemics</p>
</li>
<li>
<h3><a href="https://www.kiyomizudera.or.jp/en/" target="_blank" rel="noopener noreferrer">Kiyomizu-dera Temple</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/kiyomizudera.jpg" alt="Kiyomizu-dera Temple">
<p>Perched on the eastern hills of Kyoto, Kiyomizu-dera has stood as a beacon of Buddhist spirituality since its founding in 778 AD. Renowned for its majestic wooden stage, constructed without the use of nails, the temple offers panoramic views of Kyoto's cherry blossoms in spring and fiery foliage in autumn</p>
</li>
<li>
<h3><a href="https://toji.or.jp/" target="_blank" rel="noopener noreferrer">To-ji Temple</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/toji.jpg" alt="To-ji Temple">
<p>Dating back to the early Heian period, To-ji is an ancient Buddhist temple famous for its five-story pagoda, standing as the tallest wooden tower in Japan</p>
</li>
<li>
<h3><a href="https://www.tenryuji.com/en/" target="_blank" rel="noopener noreferrer">Tenryu-ji Temple (Arashiyama)</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/tenryu-ji.jpg" alt="Tenryu-ji Temple (Arashiyama)">
<p>Founded in 1339 by shogun Ashikaga Takauji (1305–1358), Tenryu-ji Temple in Arashiyama features one of Japan's oldest Zen temple gardens. Adjacent to the temple lies an enchanting bamboo grove, adding to the area's allure</p>
</li>
<li>
<h3><a href="https://www.sagano-kanko.co.jp/en/" target="_blank" rel="noopener noreferrer">Sagano Romantic Train (Arashiyama)</a></h3>
<p>Scenic railway journey through the picturesque Hozugawa River valley, especially stunning during the autumn foliage season</p>
</li>
<li>
<h3><a href="https://inari.jp/en/" target="_blank" rel="noopener noreferrer">Fushimi Inari Taisha Shrine</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/fushimiinari.jpg" alt="Fushimi Inari Taisha Shrine">
<p>Established in 711 AD, Fushimi Inari Taisha Shrine is renowned for its thousands of vermilion torii gates winding through forest paths on Mount Inari. These gates, donated over centuries, symbolize prayers for success and prosperity</p>
</li>
<li>
<h3><a href="https://www.byodoin.or.jp/en/" target="_blank" rel="noopener noreferrer">Byodo-in Temple (Uji City)</a></h3>
<img src="/img/tourism/prefecture/26_Kyoto/byodo-in.jpg" alt="Byodo-in Temple (Uji City)">
<p>Boasts the iconic Phoenix Hall, a national treasure of Japan depicted on the 10 yen coin. Built in 1053 during the Heian period, it stands as a masterpiece of Buddhist architecture, symbolizing Japan's rich cultural legacy</p>
</li>
<li>
<h3><a href="https://kyotomm.jp/en/" target="_blank" rel="noopener noreferrer">Kyoto International Manga Museum</a></h3>
<p>Dedicated to manga, Japan's popular comic book art form, featuring a vast collection of manga and interactive exhibits</p>
</li>
<li>
<h3><a href="https://global.toei-eigamura.com/" target="_blank" rel="noopener noreferrer">Toei Kyoto Studio Park</a></h3>
